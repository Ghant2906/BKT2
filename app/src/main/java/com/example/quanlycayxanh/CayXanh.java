package com.example.quanlycayxanh;

public class CayXanh {
    String tenKhoaHoc, tenThuongGoi, dacTinh, mauLa, hinhAnh;

    public CayXanh() {
    }

    public CayXanh(String tenKhoaHoc, String tenThuongGoi, String dacTinh, String mauLa, String hinhAnh) {
        this.tenKhoaHoc = tenKhoaHoc;
        this.tenThuongGoi = tenThuongGoi;
        this.dacTinh = dacTinh;
        this.mauLa = mauLa;
        this.hinhAnh = hinhAnh;
    }
}
